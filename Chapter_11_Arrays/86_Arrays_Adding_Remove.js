let arr = [1, 2, 3];
console.log(arr);

//Add to END
arr.push(4);
console.log(arr);

///Remove From END
arr.pop();
console.log(arr);

arr.push(5, 6);
console.log(arr);

//Add to BEGINNING
arr.unshift(0);
console.log(arr);

//Remove from Beginning
arr.shift();
console.log(arr);

console.log(arr);
arr.unshift(100);
console.log(arr);
arr.shift();
console.log(arr);
